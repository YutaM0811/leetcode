import java.util.*;

// 225. Implement Stack using Queues
class MyStack {
    private Deque<Integer> q1;
    private Deque<Integer> q2;

    public MyStack() {
        q1=new ArrayDeque<>();
        q2=new ArrayDeque<>();
    }

    public void push(int x) {
        q2.addLast(x);
        while(!q1.isEmpty()) {
            q2.addLast(q1.poll());
        }
        Deque<Integer> tmp=q1;
        q1=q2;
        q2=tmp;
    }

    public int pop() {
        return q1.poll();
    }

    public int top() {
        return q1.peek();
    }

    public boolean empty() {
        return q1.isEmpty();
    }
}
